package com.HRMain.Page.domain;

public class CalendarDTO {

	private int newcalendar_idx;
	private int yy;
	private int mm;
	private int dd;
	private String content;
	
	public int getNewcalendar_idx() {
		return newcalendar_idx;
	}
	public void setNewcalendar_idx(int newcalendar_idx) {
		this.newcalendar_idx = newcalendar_idx;
	}
	public int getYy() {
		return yy;
	}
	public void setYy(int yy) {
		this.yy = yy;
	}
	public int getMm() {
		return mm;
	}
	public void setMm(int mm) {
		this.mm = mm;
	}
	public int getDd() {
		return dd;
	}
	public void setDd(int dd) {
		this.dd = dd;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	

}
